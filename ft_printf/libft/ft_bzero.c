#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*x;
	size_t			i;

	i = 0;
	x = (unsigned char *)s;
	while (i < n)
	{
		x[i] = '\0';
		i++;
	}
}
